import type { File } from '@/types';

interface LayoutItem {
  file: File;
  top: number;
  left: number;
  width: number;
  height: number;
}

const GAP = 3;
// const MAX_W = 420;
const MAX_H = 560;
const MIN_W = 0;
const MIN_H = 200;
const RATIO_3_TOP = 0.62;

export function generateLayout(files: File[], MAX_W = 432): LayoutItem[] {
  const count = files.length;
  if (count === 0) return [];

  const result: LayoutItem[] = [];
  const W = Math.min(MAX_W, Math.max(MIN_W, MAX_W));
  const H = Math.min(MAX_H, Math.max(MIN_H, W));

  if (count === 1) {
    const file = files[0];
    const imgWidth = file.width || W;
    const imgHeight = file.height || H;
    const ratio = imgWidth / imgHeight;

    let width = imgWidth;
    let height = imgHeight;

    if (width > MAX_W) {
      width = MAX_W;
      height = width / ratio;
    }
    if (height > MAX_H) {
      height = MAX_H;
      width = height * ratio;
    }

    width = Math.max(width, MIN_W);
    height = Math.max(height, MIN_H);

    result.push({ file, top: 0, left: 0, width, height });
    return result;
  }

  if (count === 2) {
    const half = (W - GAP) / 2;
    result.push({ file: files[0], top: 0, left: 0, width: half, height: H });
    result.push({
      file: files[1],
      top: 0,
      left: half + GAP,
      width: half,
      height: H,
    });
    return result;
  }

  if (count === 3) {
    const topH = H * RATIO_3_TOP;
    const bottomH = H - topH - GAP;
    const half = (W - GAP) / 2;

    result.push({ file: files[0], top: 0, left: 0, width: W, height: topH });
    result.push({
      file: files[1],
      top: topH + GAP,
      left: 0,
      width: half,
      height: bottomH,
    });
    result.push({
      file: files[2],
      top: topH + GAP,
      left: half + GAP,
      width: half,
      height: bottomH,
    });
    return result;
  }

  if (count === 4) {
    const topH = H * 0.45;
    const midH = H * 0.28;
    const bottomH = H - topH - midH - GAP * 2;

    result.push({ file: files[0], top: 0, left: 0, width: W, height: topH });
    result.push({
      file: files[1],
      top: topH + GAP,
      left: 0,
      width: W * 0.4 - GAP,
      height: midH,
    });
    result.push({
      file: files[2],
      top: topH + GAP,
      left: W * 0.4,
      width: W * 0.6,
      height: midH,
    });
    result.push({
      file: files[3],
      top: topH + midH + GAP * 2,
      left: 0,
      width: W,
      height: bottomH,
    });
    return result;
  }

  const rows = Math.ceil(Math.sqrt(count));
  let currentIndex = 0;
  let top = 0;

  for (let row = 0; row < rows; row++) {
    const remaining = count - currentIndex;
    const remainingRows = rows - row;
    const itemsInRow = Math.ceil(remaining / remainingRows);

    const width = (W - GAP * (itemsInRow - 1)) / itemsInRow;
    const height = (H - GAP * (rows - 1)) / rows;

    for (let col = 0; col < itemsInRow; col++) {
      if (currentIndex >= count) break;

      const left = col * (width + GAP);
      result.push({
        file: files[currentIndex],
        top,
        left,
        width,
        height,
      });

      currentIndex++;
    }

    top += height + GAP;
  }

  return result;
}
